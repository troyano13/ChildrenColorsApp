    private void llenarTabla(){
        final String []columnas = {"ID", "NOMBRES", "APELLIDOS", "USUARIO", "FECHA NACIMIENTO"};
        DefaultTableModel tableModel = new DefaultTableModel(columnas, 0){
            @Override
            public boolean isCellEditable(int row, int column) {
               return false;
            }
        };
        
        jTable1.setModel(tableModel);
        jTable1.getTableHeader().setReorderingAllowed(false);        
        final List<Estudiante> listaEstudiantes = jpa.buscarEstudiantes();
        System.out.println("Total Estudiantes ========= "+listaEstudiantes.size());
        for (Estudiante estudiante : listaEstudiantes) {
            Object[] obj = new Object[columnas.length];
            obj[0] = estudiante.getId();
            obj[1] = estudiante.getNombre();
            obj[2] = estudiante.getApellido();
            obj[3] = estudiante.getUsername();
            obj[4] = estudiante.getFechaNacimiento();
            tableModel.addRow(obj);
        }

    }
