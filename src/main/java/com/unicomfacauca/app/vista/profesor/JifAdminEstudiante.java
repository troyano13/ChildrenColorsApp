    private void btnGuardarActionPerformed(ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        estudiante.setNombre(txtNombres.getText());
        estudiante.setApellido(txtApellidos.getText());
        estudiante.setUsername(txtUsername.getText());
        estudiante.setFechaNacimiento(dateFechaNacimiento.getDate());
        if(estaEditando){
            if(txtClave.getText() != null && !txtClave.getText().isEmpty()){
                estudiante.setClave(txtClave.getText());
            }
            try {
                jpa.editar(estudiante);
                Logger.getLogger(JifAdminEstudiante.class.getName()).log(Level.INFO, "Exito Al editar ======= ");
            } catch (Exception ex) {
                System.out.println("NO se pudo editar ================ ");
                Logger.getLogger(JifAdminEstudiante.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            estudiante.setClave(txtClave.getText());
            jpa.crear(estudiante);
            Logger.getLogger(JifAdminEstudiante.class.getName()).log(Level.INFO, "Exito Al REGIISTRAR !!! ======= ");
        }        
        limpiarCampos();
        llenarTabla();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jTable1MouseClicked(MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        estaEditando = false;
        int index = jTable1.getSelectedRow();
        final Object personaIdObjeto = jTable1.getValueAt(index, 0);
        final Integer  personaId = Integer.parseInt(personaIdObjeto.toString());
        estudiante = jpa.buscarEstudiantePorId(personaId);
        if(estudiante != null){
            txtNombres.setText(estudiante.getNombre());
            txtApellidos.setText(estudiante.getApellido());
            txtUsername.setText(estudiante.getUsername());            
            dateFechaNacimiento.setDate(estudiante.getFechaNacimiento());
            estaEditando = true;
            btnGuardar.setText("Actualizar");
        }
                
        
    }//GEN-LAST:event_jTable1MouseClicked

